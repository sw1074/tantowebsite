//button1マウスオーバー時の処理
function button1_On(){
    var obj = document.getElementById("button1");
    obj.src = "images/button_on.png";
}
//button1マウスアウト時の処置
function button1_Off(){
    var obj = document.getElementById("button1");
    obj.src = "images/button.jpg"
}

